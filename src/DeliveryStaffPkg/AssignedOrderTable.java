    public void AssignedOrderTable(int arrayIndext){
        
        indext = arrayIndext;
        String c[] = {"Order ID" , "StaffID " , "CustomerID ","Customer Name" 
                    , "Receiver Name" , "Receiver Address", "Receiver Phone","Status", "Payment", "Feedback", "Rate"};
        DefaultTableModel table1;
        table1 = new DefaultTableModel(0,10);
        table1.setColumnIdentifiers(c);
        jTable1.setModel(table1);
        String order [] = new String [OrderDetails.getOrderlist().size()];
        String staffid=DeliveryStaffDetails.getStafflist().get(arrayIndext).getId();
        
        String sID;
        for (int i=0; i<OrderDetails.getOrderlist().size(); i++){
            sID = OrderDetails.getOrderlist().get(i).getStaffID();
            
            if(staffid.equals(sID)){
                order[i]=OrderDetails.getOrderlist().get(i).getOrderID() + ":"
                        + OrderDetails.getOrderlist().get(i).getStaffID() + ":"
                        + OrderDetails.getOrderlist().get(i).getCustomerID() + ":"
                        + OrderDetails.getOrderlist().get(i).getCustomerName() + ":"
                        + OrderDetails.getOrderlist().get(i).getReceiverName() + ":"
                        + OrderDetails.getOrderlist().get(i).getReceiverAddress() + ":"
                        + OrderDetails.getOrderlist().get(i).getReceiverPhone() + ":"
                        + OrderDetails.getOrderlist().get(i).getStatus()+":"
                        + OrderDetails.getOrderlist().get(i).getPayment()+":"
                        + OrderDetails.getOrderlist().get(i).getFeedback() + ":"
                        + OrderDetails.getOrderlist().get(i).getRate();
                table1.addRow(order[i].split(":"));
            }
                     
        }
        
    }

