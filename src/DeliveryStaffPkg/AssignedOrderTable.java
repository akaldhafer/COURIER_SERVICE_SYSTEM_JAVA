    private void updateAndRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAndRefreshActionPerformed
        // TODO add your handling code here:
        int statusIndext= (int) jComboBox1.getSelectedIndex();
        
        String status;  
        if(statusIndext == 0){
            status = "Delivered";
        }
        else{
            status = "Undelivered";
        }
        AdminManagement ad = new AdminManagement();
        int GetRecord = ad.searchOrder2(Orderid.getText(),OrderDetails.getOrderlist().get(indext).getStaffID());
        if (GetRecord == -1){
             JOptionPane.showMessageDialog(null, "Order ID is not inside your order list", "Error Message", JOptionPane.ERROR_MESSAGE);      
        }
        else {
            String OrderID = OrderDetails.getOrderlist().get(GetRecord).getOrderID();
            String StaffID = OrderDetails.getOrderlist().get(GetRecord).getStaffID();
            String CustomerID = OrderDetails.getOrderlist().get(GetRecord).getCustomerID();
            String CustomerName = OrderDetails.getOrderlist().get(GetRecord).getCustomerName();
            String ReceiverName = OrderDetails.getOrderlist().get(GetRecord).getReceiverName();
            String ReceiverAddress = OrderDetails.getOrderlist().get(GetRecord).getReceiverAddress();
            String ReceiverPhone = OrderDetails.getOrderlist().get(GetRecord).getReceiverPhone();
            String Feedback = OrderDetails.getOrderlist().get(GetRecord).getFeedback();
            int Payment =  OrderDetails.getOrderlist().get(GetRecord).getPayment();
            int Rate =  OrderDetails.getOrderlist().get(GetRecord).getRate();
           
            try {
                ad.editOrder(GetRecord, OrderID,CustomerName, ReceiverName,
                        CustomerID,StaffID, ReceiverPhone,
                        ReceiverAddress,Feedback, status,Payment, Rate);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AssignedOrderTable.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            setVisible(false);
            AssignedOrderTable sd = new AssignedOrderTable();
            sd.AssignedOrderTable(indext);       
            sd.setVisible(true);
            sd.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_updateAndRefreshActionPerformed

